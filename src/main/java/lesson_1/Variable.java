package lesson_1;

public class Variable {
    public static void main(String[] args) {

        byte myByte = 1;  // -128 до 127
        short myShort = 1;
        int myInteger = 1;
        long myLong = 2l;
        char myChar = 'a';
        float myFloat = 12.2f;
        double myDouble = 23.2;
        boolean myBoolean = true;

        System.out.println( myByte + "," + myShort + "," + myInteger + "," + myLong + "," +
                myChar + "," + myFloat + "," + myDouble + "," + myBoolean);

    }

}
